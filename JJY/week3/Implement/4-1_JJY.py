# Idea
# 그냥 적자. -> 일단 생각나는 걸 적자. 적다보면 나올지도..
# 다음을 만족한다.
# 1) 각 문자마다 +1, -1 됨
# 2) 막히는 곳이 있음 < 1보다 작으면/크면 아예 if문을 타지 않도록!

# 지도의 크기 N 입력 받기
N = int(input("공간의 크기 N을 입력하세요: "))

# 여행자의 초기 위치
x, y = 1, 1

# 여행 계획서 입력 받기
plans = input("여행가의 이동 계획서를 입력하세요 (L, R, U, D로 이루어진 문자열): ").split()

# 이동 방향에 따른 좌표 변경
for plan in plans:
    if plan == 'L' and y > 1:  # 왼쪽으로 이동
        y -= 1
    elif plan == 'R' and y < N:  # 오른쪽으로 이동
        y += 1
    elif plan == 'U' and x > 1:  # 위로 이동
        x -= 1
    elif plan == 'D' and x < N:  # 아래로 이동
        x += 1

# 최종 위치 출력
print(f"여행자 A의 최종 위치: {x} {y}")